num1= float(input("Enter first number:"))
num2= float(input("Enter second number:"))
operator= input("What operation do you wanna perform on the numbers?:")

if operator== "+" :
    print(num1+num2)
elif operator== "-":
    print(num1-num2)
elif operator== "%":
    print (num1%num2)
elif operator== "/":
    print(num1/num2)
elif operator== "*":
    print(num1*num2)
else:
    print("Enter a valid sign of operation")



